//Arithmetic calculation
let a = 10;
let b = 3;

//Addition
let sum = a + b;
console.log("This is the sum of a and b", sum);

//Subtraction
let difference = b - a;
console.log("This is the difference of a and b", difference);

//Multiplication
let product = a * b;
console.log("This is the product of a and b", product);

//Division
let quotient = a / b;
console.log("This is the divisor of a and b", quotient);

//Modulo / Remainder
let remainder = 12 % 5;
console.log("The remainder of 12 and 5 is", remainder);